import { useDispatch } from "react-redux";
import { login } from "../redux/slices/userSlice";
import { useNavigate } from "react-router-dom";

const Login = () => {
  const [username, setUsername] = useState("");
  const [password, setPassword] = useState("");
  const [error, setError] = useState("");
  const [loading, setLoading] = useState(false);
  const [showResetModal, setShowResetModal] = useState(false);
  const [newPassword, setNewPassword] = useState("");
  const [confirmPassword, setConfirmPassword] = useState("");
  const [showPassword, setShowPassword] = useState(false);

  const dispatch = useDispatch();
  const navigate = useNavigate();

  // X·ª≠ l√Ω ƒëƒÉng nh·∫≠p
  const handleLogin = async (e) => {
    e.preventDefault();
    setLoading(true);
    setError("");

    try {
      const response = await fetch("https://phamdangtuc-001-site1.ntempurl.com/api/users/login", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ username, password }),
      });

      const textResponse = await response.text();
      console.log("üì© Ph·∫£n h·ªìi t·ª´ API:", textResponse);

      if (!response.ok) {
        throw new Error("Sai t√†i kho·∫£n ho·∫∑c m·∫≠t kh·∫©u!");
      }

      let token = textResponse.trim(); // JWT token

      if (!token.startsWith("ey")) {
        try {
          const data = JSON.parse(textResponse);
          throw new Error(data.message || "L·ªói ph·∫£n h·ªìi t·ª´ m√°y ch·ªß!");
        } catch {
          throw new Error("Ph·∫£n h·ªìi kh√¥ng h·ª£p l·ªá: " + textResponse);
        }
      }

      localStorage.setItem("token", token);

      // Gi·∫£i m√£ token l·∫•y quy·ªÅn (role)
      const decodedToken = JSON.parse(atob(token.split(".")[1]));
      const roleKey = Object.keys(decodedToken).find((key) => key.includes("role"));
      const role = decodedToken[roleKey];

      dispatch(login({ user: { username }, role, token }));

      // ƒêi·ªÅu h∆∞·ªõng theo role
      navigate(role.toLowerCase() === "admin" ? "/admin" : role.toLowerCase() === "staff" ? "/staff" : "/");

    } catch (err) {
      setError(err.message);
    } finally {
      setLoading(false);
    }
  };

  // X·ª≠ l√Ω ƒë·ªïi m·∫≠t kh·∫©u
  const handleResetPassword = async () => {
    if (!username) {
      setError("Vui l√≤ng nh·∫≠p t√™n ƒëƒÉng nh·∫≠p.");
      return;
    }
    if (newPassword !== confirmPassword) {
      setError("M·∫≠t kh·∫©u x√°c nh·∫≠n kh√¥ng kh·ªõp!");
      return;
    }

try {
        const response = await fetch("https://phamdangtuc-001-site1.ntempurl.com/api/users/change-password", {
          method: "POST",
          headers: { "Content-Type": "application/json" },
          body: JSON.stringify({ username, password: newPassword, confirmPassword }),
        });
  
        const data = await response.json();
        if (response.ok) {
          alert("M·∫≠t kh·∫©u ƒë√£ ƒë∆∞·ª£c c·∫≠p nh·∫≠t th√†nh c√¥ng!");
          setShowResetModal(false);
        } else {
          throw new Error(data.message || "C·∫≠p nh·∫≠t m·∫≠t kh·∫©u th·∫•t b·∫°i!");
        }
      } catch (err) {
        setError(err.message);
      }
    };

  return (
    <div className="flex items-center justify-center min-h-screen bg-gray-50">
      <div className="p-8 max-w-md w-full bg-white shadow-md rounded-lg">
        <h2 className="text-3xl font-bold mb-6 text-center">ƒêƒÉng nh·∫≠p</h2>
        <form onSubmit={handleLogin} className="space-y-4">
          <input
            type="text"
            placeholder="T√™n ƒëƒÉng nh·∫≠p"
            value={username}
            onChange={(e) => setUsername(e.target.value)}
            className="w-full p-2 border rounded"
            required
            autoFocus
          />
          <div className="relative">
            <input
              type={showPassword ? "text" : "password"}
              placeholder="M·∫≠t kh·∫©u"
              value={password}
              onChange={(e) => setPassword(e.target.value)}
              className="w-full p-2 border rounded"
              required
            />
            <button
              type="button"
              onClick={() => setShowPassword(!showPassword)}
              className="absolute right-2 top-2 text-gray-500"
            >
              {showPassword ? "üëÅÔ∏è" : "üîí"}
            </button>
          </div>
          {error && <p className="text-red-500">{error}</p>}
          <button
            type="submit"
            className="bg-blue-600 text-white py-2 px-4 rounded w-full hover:bg-blue-700 transition"
            disabled={loading}
          >
            {loading ? "ƒêang ƒëƒÉng nh·∫≠p..." : "ƒêƒÉng nh·∫≠p"}
          </button>
        </form>

        <div className="text-center mt-4">
          <button onClick={() => setShowResetModal(true)} className="text-blue-500 underline">
            Qu√™n m·∫≠t kh·∫©u?
          </button>
        </div>
      </div>

      {/* Modal Qu√™n M·∫≠t Kh·∫©u */}
      {showResetModal && (
        <div className="fixed inset-0 flex items-center justify-center bg-gray-800 bg-opacity-50">
          <div className="bg-white p-6 rounded shadow-lg w-96">
            <h2 className="text-xl font-bold mb-4">Qu√™n m·∫≠t kh·∫©u</h2>
            <input
              type="text"
              placeholder="T√™n ƒëƒÉng nh·∫≠p"
              value={username}
              onChange={(e) => setUsername(e.target.value)}
              className="w-full p-2 border rounded mb-3"
              required
            />
            <input
              type="password"
              placeholder="M·∫≠t kh·∫©u m·ªõi"
              value={newPassword}
              onChange={(e) => setNewPassword(e.target.value)}
              className="w-full p-2 border rounded mb-3"
              required
            />
            <input
              type="password"
              placeholder="X√°c nh·∫≠n m·∫≠t kh·∫©u m·ªõi"
              value={confirmPassword}
              onChange={(e) => setConfirmPassword(e.target.value)}
              className="w-full p-2 border rounded mb-3"
              required
            />
            {error && <p className="text-red-500">{error}</p>}
            <button
              onClick={handleResetPassword}
              className="bg-blue-600 text-white py-2 px-4 rounded w-full hover:bg-blue-700 transition mb-2"
            >
              C·∫≠p nh·∫≠t m·∫≠t kh·∫©u
            </button>
            <button
              onClick={() => setShowResetModal(false)}
              className="bg-gray-500 text-white py-2 px-4 rounded w-full hover:bg-gray-600 transition"
            >
              H·ªßy
            </button>
          </div>
        </div>
      )}
    </div>
  );
};

export default Login;
